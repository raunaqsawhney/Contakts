package com.raunaqsawhney.contakts;

public class Group {
	
	private String name; 
	private String id; 
	
	// Setters
    public void setGroupName(String name) {
        this.name = name;
    }
    
    public void setGroupID(String id) {
        this.id = id;
    }
   
    // Getters
    public String getGroupName() {
        return name;
    }
    
    public String getGroupID() {
        return id;
    }

}
